async function main() {
  const { data: tutor, error: te } = await supabase
    .from(`${PFX}tutors`)
    .select('id')
    .eq('email', DEMO_EMAIL)
    .single();
  if (te || !tutor) throw new Error(`Репетитор не найден: ${te?.message}`);
  const tutorId = tutor.id;

  const { data: student, error: se } = await supabase
    .from(`${PFX}students`)
    .select('id')
    .eq('tutor_id', tutorId)
    .eq('name', 'Михаил Кузнецов')
    .single();
  if (se || !student) throw new Error(`Ученик не найден: ${se?.message}`);
  const studentId = student.id;

  // Найти последний завершённый урок Михаила
  const { data: lessons, error: le } = await supabase
    .from(`${PFX}lessons`)
    .select('id, topic, scheduled_at')
    .eq('tutor_id', tutorId)
    .eq('student_id', studentId)
    .in('status', ['completed', 'attended'])
    .order('scheduled_at', { ascending: false })
    .limit(1);

  if (le || !lessons || lessons.length === 0) throw new Error(`Уроки не найдены: ${le?.message}`);
  const lesson = lessons[0];
  console.log(`  Урок: "${lesson.topic}" (${lesson.scheduled_at})`);

  const recordedAt = new Date(new Date(lesson.scheduled_at).getTime() + 2 * 60 * 60 * 1000 + 10 * 60 * 1000);

  // Удаляем старые записи студента
  await supabase
    .from(`${PFX}lesson_recordings`)
    .delete()
    .eq('tutor_id', tutorId)
    .eq('student_id', studentId);

  const { error: insertErr } = await supabase
    .from(`${PFX}lesson_recordings`)
    .insert({
      tutor_id: tutorId,
      student_id: studentId,
      lesson_id: lesson.id,
      title: `Михаил Кузнецов — Простые числа и решето Эратосфена`,
      source: 'upload',
      audio_path: '/recordings/2026-04-25-mikhail-prime-numbers.mp3',
      duration_sec: 7200,
      transcript: TRANSCRIPT,
      summary: SUMMARY,
      notes: NOTES,
      status: 'ready',
      recorded_at: recordedAt.toISOString(),
    });

  if (insertErr) throw new Error(`Ошибка вставки записи: ${insertErr.message}`);

  console.log(`✅ Шаг 10: 1 запись урока с транскрипцией (${Math.round(TRANSCRIPT.length / 100) * 100} симв., ${NOTES.keyPoints.length} key points)`);
}

main().catch((e) => { console.error(e); process.exit(1); });
