async function main() {
  const tutorId = await getTutorId();

  // Удаляем старые планы
  await supabase.from(`${PFX}saved_lesson_plans`).delete().eq('tutor_id', tutorId);

  const rows = PLANS.map((p) => ({
    tutor_id: tutorId,
    title: p.title,
    subject: p.subject,
    grade: p.grade,
    lesson_type: p.lesson_type,
    duration: p.duration,
    plan_json: p.plan_json,
  }));

  const { error } = await supabase.from(`${PFX}saved_lesson_plans`).insert(rows);
  if (error) throw new Error(`Ошибка вставки планов: ${error.message}`);

  console.log(`✅ Шаг 8: ${rows.length} планов уроков`);
}

main().catch((e) => { console.error(e); process.exit(1); });
