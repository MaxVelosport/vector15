async function main() {
  const tutorId = await getTutorId();

  // Удаляем старые задачи
  await supabase.from(`${PFX}tasks`).delete().eq('tutor_id', tutorId);

  const rows = TASKS.map((t) => ({
    tutor_id: tutorId,
    topic: t.topic,
    difficulty: t.difficulty,
    task: t.task,
    solution: t.solution,
    answer: t.answer,
  }));

  const { error } = await supabase.from(`${PFX}tasks`).insert(rows);
  if (error) throw new Error(`Ошибка вставки задач: ${error.message}`);

  const bySubject = {
    math: rows.filter((r) => r.topic.startsWith('Математика')).length,
    physics: rows.filter((r) => r.topic.startsWith('Физика')).length,
    cs: rows.filter((r) => r.topic.startsWith('Информатика')).length,
  };

  console.log(`✅ Шаг 9: ${rows.length} задач (математика ${bySubject.math}, физика ${bySubject.physics}, информатика ${bySubject.cs})`);
}

main().catch((e) => { console.error(e); process.exit(1); });
