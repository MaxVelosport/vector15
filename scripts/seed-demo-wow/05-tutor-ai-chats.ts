async function main() {
  const tutorId = await getTutorId();

  // Удаляем старые чаты репетитора
  const { data: oldChats } = await supabase
    .from(`${PFX}tutor_ai_chats`)
    .select('id')
    .eq('tutor_id', tutorId);

  if (oldChats && oldChats.length > 0) {
    const ids = oldChats.map((c: { id: string }) => c.id);
    await supabase
      .from(`${PFX}tutor_ai_chat_messages`)
      .delete()
      .in('chat_id', ids);
    await supabase
      .from(`${PFX}tutor_ai_chats`)
      .delete()
      .eq('tutor_id', tutorId);
  }

  let totalMessages = 0;

  for (const chat of CHATS) {
    const chatCreated = new Date(chat.createdAt);

    const { data: chatRow, error: chatErr } = await supabase
      .from(`${PFX}tutor_ai_chats`)
      .insert({
        tutor_id: tutorId,
        title: chat.title,
        created_at: chatCreated.toISOString(),
        updated_at: chatCreated.toISOString(),
      })
      .select('id')
      .single();

    if (chatErr || !chatRow) throw new Error(`Ошибка создания чата: ${chatErr?.message}`);

    const chatId = chatRow.id;

    const msgs = chat.messages.map((m) => {
      const msgDate = new Date(chatCreated.getTime() + m.offsetMinutes * 60_000);
      return {
        chat_id: chatId,
        tutor_id: tutorId,
        role: m.role,
        content: m.content,
        created_at: msgDate.toISOString(),
      };
    });

    const { error: msgErr } = await supabase
      .from(`${PFX}tutor_ai_chat_messages`)
      .insert(msgs);

    if (msgErr) throw new Error(`Ошибка вставки сообщений: ${msgErr.message}`);

    totalMessages += msgs.length;
    console.log(`  ✅ "${chat.title}" — ${msgs.length} сообщений`);
  }

  console.log(`\n✅ Шаг 5: ${CHATS.length} чатов, ${totalMessages} сообщений`);
}

main().catch((e) => { console.error(e); process.exit(1); });
