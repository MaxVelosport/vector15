import * as dotenv from 'dotenv';
dotenv.config();
import { supabase } from '../../server/supabase';

const PFX = 'Tvoy_vector_2_';
const DEMO_EMAIL = 'demo@vector.ru';

async function getTutorId(): Promise<string> {
  const { data, error } = await supabase
    .from(`${PFX}tutors`)
    .select('id')
    .eq('email', DEMO_EMAIL)
    .single();
  if (error || !data) throw new Error(`Репетитор не найден: ${error?.message}`);
  return data.id;
}

async function getStudentId(tutorId: string, name: string): Promise<string> {
  const { data, error } = await supabase
    .from(`${PFX}students`)
    .select('id')
    .eq('tutor_id', tutorId)
    .eq('name', name)
    .single();
  if (error || !data) throw new Error(`Ученик не найден: ${name} — ${error?.message}`);
  return data.id;
}

