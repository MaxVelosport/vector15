async function main() {
  const tutorId = await getTutorId();

  // Собираем все student_id нужных учеников
  const studentNames = [...new Set(CHATS.map((c) => c.studentName))];
  const studentMap: Record<string, string> = {};
  for (const name of studentNames) {
    studentMap[name] = await getStudentId(tutorId, name);
  }

  // Удаляем старые чаты учеников
  for (const [, sid] of Object.entries(studentMap)) {
    const { data: oldChats } = await supabase
      .from(`${PFX}ai_chats`)
      .select('id')
      .eq('student_id', sid);

    if (oldChats && oldChats.length > 0) {
      const ids = oldChats.map((c: { id: string }) => c.id);
      await supabase.from(`${PFX}ai_chat_messages`).delete().in('chat_id', ids);
      await supabase.from(`${PFX}ai_chats`).delete().in('id', ids);
    }
  }

  let totalMessages = 0;

  for (const chat of CHATS) {
    const studentId = studentMap[chat.studentName];
    const chatCreated = new Date(chat.createdAt);

    const { data: chatRow, error: chatErr } = await supabase
      .from(`${PFX}ai_chats`)
      .insert({
        student_id: studentId,
        title: chat.title,
        created_at: chatCreated.toISOString(),
        updated_at: chatCreated.toISOString(),
      })
      .select('id')
      .single();

    if (chatErr || !chatRow) throw new Error(`Ошибка создания чата: ${chatErr?.message}`);

    const chatId = chatRow.id;

    const msgs = chat.messages.map((m) => {
      const msgDate = new Date(chatCreated.getTime() + m.offsetMinutes * 60_000);
      return {
        chat_id: chatId,
        student_id: studentId,
        role: m.role,
        content: m.content,
        created_at: msgDate.toISOString(),
      };
    });

    const { error: msgErr } = await supabase
      .from(`${PFX}ai_chat_messages`)
      .insert(msgs);

    if (msgErr) throw new Error(`Ошибка вставки сообщений: ${msgErr.message}`);

    totalMessages += msgs.length;
    console.log(`  ✅ [${chat.studentName}] "${chat.title}" — ${msgs.length} сообщений`);
  }

  console.log(`\n✅ Шаг 6: ${CHATS.length} чатов, ${totalMessages} сообщений`);
}

main().catch((e) => { console.error(e); process.exit(1); });
